package ch08.logicalOperators

/**
 * @author : choi-ys
 * @date : 2021/06/14 1:53 오후
 * @apiNote : 논리 연산자 = 논리 값을 이용하여 새로운 논리 값을 도출
 *  - && : and
 *  - || : or
 *  - ! : not
 */
fun main(){
    println("true && false : " + (true && false))
    println("true || false : " + (true || false))
    println("!true : " + !true)
    println("!false : " + !false)
}
